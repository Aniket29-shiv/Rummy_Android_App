    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getRedeem_id() {
        return redeem_id;
    }

    public void setRedeem_id(String redeem_id) {
        this.redeem_id = redeem_id;
    }

